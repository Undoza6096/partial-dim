function clearOldAchieves(){
    var toRemove = [];
    var achieveKey;
    var values = Object.keys(allAchievements).map(function(e) { return allAchievements[e] });
    for (var i = 0; i < player.achievements.length; i++) {
        if (player.achievements[i] === null) {
			toRemove.push(i)
		} else if (values.indexOf(player.achievements[i]) !== -1 ) {  // does index[i] exist in allAchievements as a value?
            toRemove.push(i); // mark it for removal
            achieveKey = Object.keys(allAchievements).find(function(key){ return allAchievements[key] === player.achievements[i];});
            if (!hasAch(achieveKey)) { // check if new key already exists as well
                player.achievements.push(achieveKey); // if not... add it
            }
        } else if (allAchievements[player.achievements[i]] === undefined){
            var r = player.achievements[i].indexOf("r")
            if (r < 0) r = 0
            else r = parseInt(player.achievements[i].split("r")[1])
            if (r < 140) toRemove.push(i)
        }
    }

    toRemove.reverse();
    for (var i = 0; i < toRemove.length; i++) {
        player.achievements.splice(toRemove[i], 1);
    }
}

function checkAchievement(id) {
    // Some achievements have different positions from some mods. This is for NG Update to prevent anything from NG++ showing up
	if ((id == "ngpp13" || id == "ngpp18") && player.exdilation) return false

	//Mod check
	let ngp3 = id.split("ng3p")[1]
	if (id.split("ngm5p")[1]) return inNGM(5)
	if (id.split("ngud")[1]) return player.exdilation != undefined
	if (id.split("ngpp")[1]) return player.meta != undefined
	if (ngp3) return tmp.ngpX >= 3

	//Vanilla check
	let r = parseInt(id.split("r")[1])
	if (r == 105 || (r != 117 && r > 110)) return player.boughtDims === undefined

	return true
}

function checkAchievementRow(id) {
	//Mod check
	let ngp3 = id.split("ng3p")[1]
	let ngp3r = id.split("ng3pr")[1]
	if (id.split("ngm5p")[1]) return inNGM(5)
	if (id.split("ngud")[1]) return player.exdilation != undefined
	if (id.split("ngpp")[1]) return player.meta != undefined
	if (ngp3r) return tmp.ngpX >= 3
	if (ngp3) return tmp.ngpX >= 3 && ngp3 <= 3

	//Vanilla check
	let r = parseInt(id.split("r")[1])
	if (r >= 11) return player.boughtDims === undefined

	return true
}

